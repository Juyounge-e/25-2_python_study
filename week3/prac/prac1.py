def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

# 함수 호출
result1 = add(10, 5)
result2 = subtract(10, 5)
result3 = multiply(10, 5)
result4 = divide(10, 5)

print(f"10 + 5 = {result1}")
print(f"10 - 5 = {result2}")
print(f"10 * 5 = {result3}")
print(f"10 / 5 = {result4}")